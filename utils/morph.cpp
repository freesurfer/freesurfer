/*--------------------------------------------------------------------

  STOLEN from the matlab image processing toolbox.

%BWMORPH Binary image morphological operation
% C = BWMORPH(BW,LUT) filters the binary image BW by the
% filter h = 2.^[0 3 6;1 4 7;2 5 8] and uses the filtered
% result (plus 1) to look up the return pixel value in the
% vector LUT.  LUT is a 512-by-1 look up table that typically
% contains a 1 for "hit" pixel configurations and zero
% everywhere else.  The filter above produces the numbers 0
% through 511, one for each possible pixel configuration in
% a 3-by-3 neighborhood.  The binary image result is returned
% in C.
%
% C = BWMORPH(BW,'operation') applies the morphological operation
% 'operation' to the binary image BW.  The operation strings are
% used to create the appropriate LUT.  Possible operations are:
%   'bothat'  The 'close'ing minus the original image.
%   'bridge'  Bridge previously unconnected pixels.
%   'clean'   Remove isolated pixels.
%   'close'   Binary closure.  The same as 'dilate' followed by 'erode'.
%   'diag'    Diagonal fill to eliminate 8-connectivity of background.
%   'dilate'  Add pixels to the boundary of binary objects.
%   'erode'   Remove pixels from the boundary of binary objects.
%   'fatten'  Add pixels to boundary without bridging objects.
%   'fill'    Fill isolated interior pixels.
%   'hbreak'  Erase H-connected pixels.
%   'majority'Create a white pixel if 5 or more pixels are white.
%   'perim4'  4-connected perimeter pixels.
%   'perim8'  8-connected perimeter pixels.
%   'open'    Binary opening.  The same as 'erode' followed by 'dilate'.
%   'remove'  Remove interior pixels.
%   'shrink'  Shrink objects until they are points (preserves Euler number).
%   'skel'    Skeletonize objects (preserves Euler number).
%   'spur'    Remove spur pixels.
%   'thicken' Thicken objects (preserves Euler number).
%   'thin'    Thin objects until they are lines (preserves Euler number).
%   'tophat'  The original image minus its 'open'ing.
%
% BWMORPH(...,N) applies the operation N times.
%
% [C,LUT] = BWMORPH(...) returns the LUT used.  The operations that
% preserve Euler number use two LUTs, a mark and a reject LUT.  Use
% [C,MLUT,RLUT] = BWMORPH(...) to extract the two LUTs.
%

-----------------------------------------------------------------*/

