static void morphApplyLut(IMAGE *Isrc, IMAGE *Idst, unsigned char *lut)
{
  int row, col, rows, cols, *spix;
  unsigned char *dpix;  //, *endpix;

  rows = Isrc->rows;
  cols = Isrc->cols;

  dpix = (unsigned char *)IMAGEpix(Idst, 0, 0);
  // endpix = IMAGEpix(Idst, Idst->cols - 1, Idst->rows - 1);
  spix = (int *)IMAGEIpix(Isrc, 0, 0);
  for (row = 0; row < rows; row++) {
    for (col = 0; col < cols; col++) {
      *dpix++ = lut[*spix++];
    }
  }
}
